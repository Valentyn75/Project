def fib(k):
    # Базовий випадок: якщо k <= 0, повертаємо пустий список
    if k <= 0:
        return []
    # Базовий випадок: якщо k == 1, повертаємо список з одного елемента
    elif k == 1:
        return [0]
    # Базовий випадок: якщо k == 2, повертаємо список з двох перших чисел Фібоначчі
    elif k == 2:
        return [0, 1]
    else:
        # Рекурсивний випадок: обчислюємо попередні числа Фібоначчі
        fib_list = fib(k - 1)
        # Додаємо наступне число Фібоначчі до списку
        next_value = fib_list[-1] + fib_list[-2]
        fib_list.append(next_value)
        return fib_list

# Програма для виведення k чисел Фібоначчі
k = int(input("Введіть кількість чисел Фібоначчі: "))
print(fib(k))
